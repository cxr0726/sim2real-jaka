#!/usr/bin/env python3
"""
Benchmark offline GMR retargeting on a saved XRobot recording.

This script loads the npz generated by record_smplx.py, reconstructs the
per-frame human pose dictionaries expected by GMR, and reports overall FPS and
per-frame latency statistics.
"""

from __future__ import annotations

from dataclasses import dataclass
import json
import time
from pathlib import Path
from typing import Any

import numpy as np
import tyro

GMR = None


DEFAULT_BODY_JOINT_NAMES = [
    "Pelvis",
    "Left_Hip",
    "Right_Hip",
    "Spine1",
    "Left_Knee",
    "Right_Knee",
    "Spine2",
    "Left_Ankle",
    "Right_Ankle",
    "Spine3",
    "Left_Foot",
    "Right_Foot",
    "Neck",
    "Left_Collar",
    "Right_Collar",
    "Head",
    "Left_Shoulder",
    "Right_Shoulder",
    "Left_Elbow",
    "Right_Elbow",
    "Left_Wrist",
    "Right_Wrist",
    "Left_Hand",
    "Right_Hand",
]


def _load_runtime_dependencies() -> None:
    global GMR

    try:
        from general_motion_retargeting import GeneralMotionRetargeting as _GMR
    except ImportError as exc:
        raise ImportError(
            "Failed to import 'general_motion_retargeting'. Install GMR first."
        ) from exc

    GMR = _GMR


def _load_metadata(raw: np.lib.npyio.NpzFile) -> dict[str, Any]:
    if "metadata_json" not in raw:
        return {}
    value = raw["metadata_json"]
    if isinstance(value, np.ndarray):
        value = value.item()
    if isinstance(value, bytes):
        value = value.decode("utf-8")
    return json.loads(str(value))


def _normalize_joint_names(values: Any) -> list[str]:
    arr = np.asarray(values)
    return [str(item) for item in arr.tolist()]


def _frame_from_arrays(
    body_pos: np.ndarray, body_rot: np.ndarray, joint_names: list[str], idx: int
) -> dict[str, list[np.ndarray]]:
    frame: dict[str, list[np.ndarray]] = {}
    for joint_idx, joint_name in enumerate(joint_names):
        frame[joint_name] = [
            np.asarray(body_pos[idx, joint_idx], dtype=np.float32),
            np.asarray(body_rot[idx, joint_idx], dtype=np.float32),
        ]
    return frame


def _frame_from_raw_object(frame_obj: Any) -> dict[str, list[np.ndarray]]:
    if isinstance(frame_obj, dict):
        if "body_pose_dict" in frame_obj:
            body_pose_dict = frame_obj["body_pose_dict"]
            return {
                str(name): [
                    np.asarray(value[0], dtype=np.float32),
                    np.asarray(value[1], dtype=np.float32),
                ]
                for name, value in body_pose_dict.items()
            }
        return {
            str(name): [
                np.asarray(value[0], dtype=np.float32),
                np.asarray(value[1], dtype=np.float32),
            ]
            for name, value in frame_obj.items()
        }
    raise TypeError(f"Unsupported raw frame object type: {type(frame_obj)!r}")


def _load_recording(input_path: Path) -> tuple[dict[str, Any], list[dict[str, list[np.ndarray]]]]:
    with np.load(input_path, allow_pickle=True) as raw:
        metadata = _load_metadata(raw)
        joint_names = metadata.get("joint_names")
        if "body_joint_names" in raw:
            joint_names = _normalize_joint_names(raw["body_joint_names"])
        if joint_names is None:
            joint_names = list(DEFAULT_BODY_JOINT_NAMES)
        else:
            joint_names = [str(name) for name in joint_names]

        if "body_pos" in raw and "body_rot_wxyz" in raw:
            body_pos = np.asarray(raw["body_pos"], dtype=np.float32)
            body_rot = np.asarray(raw["body_rot_wxyz"], dtype=np.float32)
            if body_pos.ndim != 3 or body_rot.ndim != 3:
                raise ValueError("Expected body_pos/body_rot_wxyz to have shape [T, J, ...]")
            if body_pos.shape[0] != body_rot.shape[0]:
                raise ValueError("body_pos and body_rot_wxyz frame counts do not match")
            frames = [
                _frame_from_arrays(body_pos, body_rot, joint_names, idx)
                for idx in range(body_pos.shape[0])
            ]
        elif "frames" in raw:
            frames = [_frame_from_raw_object(frame) for frame in raw["frames"].tolist()]
        else:
            raise ValueError(
                f"{input_path} does not contain a supported recording format."
            )

    return metadata, frames


@dataclass
class Args:
    """Benchmark offline GMR retargeting."""

    input: Path
    actual_human_height: float = 1.6
    warmup_frames: int = 1
    num_frames: int = 1000
    offset_to_ground: bool = False


def main(args: Args) -> None:
    _load_runtime_dependencies()

    metadata, frames = _load_recording(args.input)
    if not frames:
        raise RuntimeError(f"No frames found in {args.input}")

    sample_fps = metadata.get("sample_fps", metadata.get("mocap_frame_rate", "unknown"))
    print(f"Loaded {len(frames)} frames from: {args.input}")
    print(f"Recording schema: {metadata.get('schema', 'unknown')}")
    print(f"Recording sample_fps: {sample_fps}")
    print(f"Warmup passes: {max(0, int(args.warmup_frames))}")
    print(f"Timed passes: {max(0, int(args.num_frames))}")

    retargeter = GMR(
        src_human="xrobot",
        tgt_robot="unitree_g1",
        actual_human_height=float(args.actual_human_height),
    )

    first_frame = frames[0]
    warmup_passes = max(0, int(args.warmup_frames))
    for _ in range(warmup_passes):
        retargeter.retarget(first_frame, offset_to_ground=bool(args.offset_to_ground))

    timed_passes = max(0, int(args.num_frames))
    timed_frames = [first_frame] * timed_passes

    timings_s: list[float] = []
    start_total = time.perf_counter()
    for frame in timed_frames:
        t0 = time.perf_counter()
        retargeter.retarget(frame, offset_to_ground=bool(args.offset_to_ground))
        timings_s.append(time.perf_counter() - t0)
    elapsed_total = time.perf_counter() - start_total

    timed_count = len(timed_frames)
    if timed_count == 0:
        print("No timed frames selected. Nothing to report.")
        return

    timings_ms = np.asarray(timings_s, dtype=np.float64) * 1000.0
    overall_fps = timed_count / elapsed_total if elapsed_total > 0.0 else float("inf")
    avg_ms = float(np.mean(timings_ms))
    median_ms = float(np.median(timings_ms))
    p90_ms = float(np.percentile(timings_ms, 90))
    p95_ms = float(np.percentile(timings_ms, 95))
    p99_ms = float(np.percentile(timings_ms, 99))
    min_ms = float(np.min(timings_ms))
    max_ms = float(np.max(timings_ms))

    print("Retarget benchmark summary")
    print(f"  timed_frames: {timed_count}")
    print(f"  elapsed_s: {elapsed_total:.6f}")
    print(f"  overall_fps: {overall_fps:.3f}")
    print(f"  avg_ms_per_frame: {avg_ms:.3f}")
    print(f"  median_ms_per_frame: {median_ms:.3f}")
    print(f"  p90_ms_per_frame: {p90_ms:.3f}")
    print(f"  p95_ms_per_frame: {p95_ms:.3f}")
    print(f"  p99_ms_per_frame: {p99_ms:.3f}")
    print(f"  min_ms_per_frame: {min_ms:.3f}")
    print(f"  max_ms_per_frame: {max_ms:.3f}")


if __name__ == "__main__":
    main(tyro.cli(Args))
